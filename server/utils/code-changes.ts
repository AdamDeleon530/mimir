export interface PrStatus {
  ok: boolean
  repo: RepoKey
  pr_number: number
  pr_url: string
  state: 'open' | 'closed'
  merged: boolean
  title: string
  branch: string
  preview_url: string | null
  preview_note?: string
}

export async function getPrStatus(repoKey: string, prNumber: number): Promise<PrStatus | { ok: false; error: string }> {
  if (!isManagedRepo(repoKey)) return { ok: false, error: `repo "${repoKey}" not managed` }
  try {
    const pr = await getPullRequest(repoKey, prNumber)
    const previewUrl = pr.state === 'open' ? await getVercelPreviewUrl(repoKey, prNumber) : null
    return {
      ok: true,
      repo: repoKey,
      pr_number: pr.number,
      pr_url: pr.html_url,
      state: pr.state,
      merged: pr.merged,
      title: pr.title,
      branch: pr.head.ref,
      preview_url: previewUrl,
      preview_note: previewUrl
        ? undefined
        : pr.state === 'open'
          ? 'Vercel preview not yet posted. Wait 30-90s and re-check.'
          : 'PR is closed — no preview.',
    }
  } catch (err) {
    return { ok: false, error: err instanceof Error ? err.message : String(err) }
  }
}

export async function listOpenPrsForRepo(repoKey: string): Promise<{ ok: boolean; repo?: RepoKey; prs?: Array<Pick<PullRequest, 'number' | 'html_url' | 'title' | 'head'>>; error?: string }> {
  if (!isManagedRepo(repoKey)) return { ok: false, error: `repo "${repoKey}" not managed` }
  try {
    const prs = await listOpenPullRequests(repoKey)
    return {
      ok: true,
      repo: repoKey,
      prs: prs.map(p => ({ number: p.number, html_url: p.html_url, title: p.title, head: p.head })),
    }
  } catch (err) {
    return { ok: false, error: err instanceof Error ? err.message : String(err) }
  }
}

export async function mergePr(repoKey: string, prNumber: number): Promise<{ ok: boolean; merged: boolean; repo?: RepoKey; pr_url?: string; message?: string }> {
  if (!isManagedRepo(repoKey)) return { ok: false, merged: false, message: `repo "${repoKey}" not managed` }
  try {
    const pr = await getPullRequest(repoKey, prNumber)
    if (pr.state !== 'open') return { ok: false, merged: pr.merged, repo: repoKey, pr_url: pr.html_url, message: `PR is ${pr.state}` }
    const result = await mergePullRequest(repoKey, prNumber, { method: 'squash' })
    return { ok: result.merged, merged: result.merged, repo: repoKey, pr_url: pr.html_url, message: result.message }
  } catch (err) {
    return { ok: false, merged: false, repo: repoKey, message: err instanceof Error ? err.message : String(err) }
  }
}

