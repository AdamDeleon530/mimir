/**
 * Code-change pipeline for Mimir.
 *
 * Adam talks to Mimir → Mimir calls propose_code_change(repo, instruction)
 *   → A small agentic sub-loop reads files, stages edits, calls done()
 *   → We atomically commit all staged edits to a new branch
 *   → Open a PR
 *   → Return PR URL + branch + diff summary + (later) Vercel preview URL
 * Adam reviews → says "ship it" → Mimir calls merge_pull_request
 *   → squash-merge, Vercel auto-deploys main
 *
 * Safety:
 *   - Repo allowlist enforced by github-client.ts
 *   - File path blocklist (no .env, no lock files, no secrets)
 *   - Max files edited per PR: 5
 *   - Max sub-loop iterations: 15
 *   - Max bytes per file: 200KB (we ignore huge files)
 *   - Squash-merge only, never force-push
 */
